<?php
  $x = 10;
  $y = 20; 
?>

<h3> Up and Down operator</h3>
<div>แสดงค่าก่อนเพิ่ม ++x : <?php echo $x; ?></div>
<div>เพิ่มก่อน ++x<?php echo ++$x;?></div>
<div>เพิ่มหลัง x++<?php echo $x++?></div>
<div>แสดงค่าเพิ่มหลัง x++ : <?php echo $x; ?></div>
<div>แสดงค่าก่อนลบ ++y : <?php echo $y; ?></div>
<div>ลบก่อน --y<?php echo --$y;?></div>
<div>ลบหลัง y--<?php echo $y--; ?></div>
<div>แสดงค่าหลังลบ y-- : <?php echo $y; ?></div>